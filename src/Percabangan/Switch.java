package Percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=0;
        switch(jalanJalan) {
            case 1:
                System.out.println("Jalanan");
                break;
            case 2:
                System.out.println("Gaduh");
                break;
            case 3:
                System.out.println("Bersama");
                break;
            case 4:
                System.out.println("Gatau pengen beli truk");
                break;
            default:
                System.out.println("Ga Ada sopannya ye :)");
                break;
        }
    }
}
